+(void)setTextFieldPadding:(UITextField *)textField paddingValue:(float)padding{
    // this method is for adding padding to uitextfeild
    UIView *leftView = [[UIView alloc] initWithFrame:CGRectMake(0, 0, padding, textField.frame.size.height)];
    leftView.backgroundColor = textField.backgroundColor;
    textField.leftView = leftView;
    textField.leftViewMode = UITextFieldViewModeAlways;
}

+(NSString*)getDocumentDirectoryPath
{
    NSArray *arrPaths = NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES);
